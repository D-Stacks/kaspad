// KaspadMessageFromAppMessage creates a KaspadMessage from a appmessage.Message
func KaspadMessageFromAppMessage(message appmessage.Message) (*KaspadMessage, error) {
	payload, err := toKaspadMessagePayload(message)
	if err != nil {
		return nil, err
	}
	return &KaspadMessage{
		Payload: payload,
	}, nil
}

