func toKaspadMessagePayload(message appmessage.Message) (isKaspadMessage_Payload, error) {
	rpcPayload, err := toRPCPayload(message)
	if err != nil {
		return nil, err
	}
	if rpcPayload != nil {
		return rpcPayload, nil
	}

	return nil, errors.Errorf("unknown message type %T", message)
}


